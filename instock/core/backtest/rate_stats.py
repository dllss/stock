# ==================== 计算收益率 ====================

"""
计算股票买入后的未来收益率
参数说明：
code_name (tuple): 股票信息
- code_name[0]：买入日期（str）
- code_name[1]：股票代码（str）
- code_name[2]：股票名称（str）
data (DataFrame): 该股票的历史K线数据
必须包含：
- date：日期
- close：收盘价
- 数据应该是完整的，从买入日期开始往后
stock_column (list): 返回数据的列名列表
- 格式：['date', 'code', 'rate_1', 'rate_3', 'rate_5', ...]
- 决定计算哪些天数的收益率
threshold (int): 计算的最大天数
- 默认101：计算未来100天（第0天是买入日）
- 如果数据不足101天，计算到最后一天
返回值：
pandas.Series: 收益率数据
- 索引：stock_column列名
- 值：['date', 'code', rate_1值, rate_3值, ...]
- 未来数据不足的位置用None填充
计算原理：
1. 提取买入日期和代码
2. 筛选出买入日期之后的数据
3. 取前N天数据（N=threshold）
4. 计算每天相对于买入价的收益率
5. 返回Series格式
执行流程：
1. 数据验证
2. 数据筛选
3. 计算收益率
4. 构建返回数据
5. 填充缺失值
使用示例：
# 假设2024-01-01买入股票600000
code_name = ('2024-01-01', '600000', '浦发银行')
hist_data = 股票的历史数据（从2024-01-01开始）
columns = ['date', 'code', 'rate_1', 'rate_3', 'rate_5']
result = get_rates(code_name, hist_data, columns, 6)
# 返回：Series(['2024-01-01', '600000', 2.5, 5.3, 3.1])
为什么从买入日期开始？
- 回测是基于历史的
- 买入日期是已知的
- 要计算买入后的收益
- 所以筛选 date >= 买入日期
"""
def get_rates(code_name, data, stock_column, threshold=101):
    try:
        # ==================== 步骤1: 数据验证 ====================
        # 检查数据是否为空
        if data is None:
            # 没有历史数据，无法计算
            return None

        # ==================== 步骤2: 提取买入信息 ====================
        start_date = code_name[0]  # 买入日期，如"2024-01-01"
        code = code_name[1]  # 股票代码，如"600000"
        
        # 初始化返回数组，开头是日期和代码
        stock_data_list = [start_date, code]

        # ==================== 步骤3: 数据筛选 ====================
        # 创建布尔掩码：date >= 买入日期
        # 只保留买入日期及之后的数据
        mask = (data['date'] >= start_date)
        data = data.loc[mask].copy()  # copy()避免警告
        
        # 只取前threshold天数据
        # head(n)：取前n行
        # 默认101天：买入日+未来100天
        data = data.head(n=threshold)

        # ==================== 步骤4: 数据充足性检查 ====================
        # 至少需要2天数据（买入日+至少1个未来日）
        if len(data.index) <= 1:
            # 数据不足，无法计算收益率
            return None

        # ==================== 步骤5: 计算收益率 ====================
        # 获取买入价格（第0天的收盘价）
        close1 = data.iloc[0]['close']  # iloc[0]：第一行
        
        # 计算每天相对于买入价的收益率
        # 公式：(当前价 - 买入价) / 买入价 * 100
        # np.around()：四舍五入
        # decimals=2：保留2位小数
        data.loc[:, 'sum_pct_change'] = np.around(
            100 * (data['close'].values - close1) / close1, 
            decimals=2
        )
        
        # 现在data有新列sum_pct_change，每一行是该天的收益率
        # 例如：
        # date        close  sum_pct_change
        # 2024-01-01  10.0   0.00（买入日）
        # 2024-01-02  10.5   5.00（rate_1）
        # 2024-01-03  10.3   3.00（rate_2）
        # 2024-01-04  11.0   10.00（rate_3）

        # ==================== 步骤6: 构建返回数据 ====================
        # 遍历数据，提取收益率
        first = True  # 标记第一行（买入日，跳过）
        col_len = len(data.columns)  # 列数
        
        # data.values：转换为numpy数组，加快遍历
        for row in data.values:
            if first:
                # 第一行是买入日，收益率为0，跳过
                first = False
            else:
                # 其他行：提取sum_pct_change列（最后一列）
                stock_data_list.append(row[col_len-1])
        
        # 现在stock_data_list包含：
        # ['2024-01-01', '600000', 5.00, 3.00, 10.00, ...]
        # 分别对应：date, code, rate_1, rate_2, rate_3, ...

        # ==================== 步骤7: 填充缺失值 ====================
        # 如果未来数据不足100天，需要填充None
        # 例如：只有50天数据，但stock_column需要100个收益率
        
        # 计算还需要多少个None
        _l = len(stock_column) - len(stock_data_list)
        
        # 填充None
        for i in range(0, _l):
            stock_data_list.append(None)
        
        # 现在stock_data_list长度与stock_column相同
        # 未来数据不足的位置用None填充

    except Exception as e:
        # 捕获并记录异常
        logging.error(f"rate_stats.get_rates处理异常：{code}代码{e}")

    # ==================== 步骤8: 返回结果 ====================
    # 将列表转换为pandas Series
    # Series类似字典，有索引（stock_column）和值（stock_data_list）
    return pd.Series(stock_data_list, index=stock_column)


