"""
收益率统计计算模块（第七层核心）
=================================
这个模块负责计算股票买入后的N日收益率，用于回测验证策略。

什么是收益率？
- 买入股票后，在未来N天的盈亏百分比
- 公式：收益率 = (未来价格 - 买入价格) / 买入价格 * 100%
- 正值：盈利，负值：亏损

为什么要计算收益率？
- 验证策略：这个策略真的赚钱吗？
- 评估风险：最大回撤是多少？
- 优化参数：什么时候买入最好？
- 对比策略：哪个策略更好？

计算的收益率：
- rate_1：买入后1天的收益率
- rate_3：买入后3天的收益率
- rate_5：买入后5天的收益率
- rate_10：买入后10天的收益率
- rate_20：买入后20天的收益率
- rate_30：买入后30天的收益率
- rate_60：买入后60天的收益率
- rate_100：买入后100天的收益率
- ... 最多计算100天

使用场景：
1. 策略回测：验证策略有效性
2. 参数优化：找到最佳买入时机
3. 风险评估：了解最大回撤
4. 策略对比：选择最好的策略

数据流程：
选股结果 → 提取日期代码 → 获取未来价格 → 计算收益率 → 更新数据库

计算示例：
假设某策略在2024-01-01选出股票A（收盘价10元）
- 2024-01-02收盘价10.5元，rate_1 = (10.5-10)/10*100 = 5%
- 2024-01-04收盘价11元，rate_3 = (11-10)/10*100 = 10%
- 2024-01-08收盘价9.5元，rate_5 = (9.5-10)/10*100 = -5%
"""

# ==================== 导入必需的库 ====================
import logging  # 日志记录
import numpy as np  # 数值计算
import pandas as pd  # 数据处理

__author__ = 'myh '
