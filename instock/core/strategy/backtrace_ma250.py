"""
回踩年线策略（第六层 - 趋势类策略）
====================================
这是一个经典的趋势跟踪策略，寻找突破年线后回踩的股票。

策略名称：回踩年线
策略类型：趋势跟踪、突破回踩
策略周期：中长期（60-250天）

策略原理：
- 股价突破年线（250日均线）
- 回调到年线附近（不跌破）
- 缩量回调（恐慌盘已出局）
- 这是一个二次买入机会

什么是年线？
- 年线：250日移动平均线
- 代表一年的平均成本（约250个交易日）
- 被认为是牛熊分界线
- 站上年线：进入上升趋势
- 跌破年线：进入下降趋势

策略条件（4个条件同时满足）：
1. 时间段划分：
   - 前段：最近60日最高价之前的交易日
   - 后段：最高价当日及之后的交易日
   
2. 前段突破：
   - 前段开始时：收盘价 < 年线（在年线下方）
   - 前段结束时：收盘价 > 年线（突破年线）
   
3. 后段回踩：
   - 后段所有日期：收盘价 >= 年线（不跌破）
   - 最低价与最高价相差10-50日
   
4. 缩量回调：
   - 最高价日成交量 / 最低价日成交量 > 2（放量上涨，缩量回调）
   - 最低价 / 最高价 < 0.8（回调幅度超过20%）

为什么这样设计？
- 前段突破：确认上升趋势已形成
- 后段不破：趋势继续有效
- 缩量回调：恐慌盘已出，筹码稳定
- 二次买入：风险较小，机会较好

策略优势：
1. 趋势确认：已突破年线，趋势明确
2. 风险可控：有年线支撑，止损明确
3. 买点明确：回踩不破是买入时机
4. 胜率较高：趋势延续概率大

策略风险：
1. 识别滞后：突破后才知道
2. 可能假突破：突破后又跌回
3. 回调太深：可能趋势已改变
4. 时间成本：等待回踩需要时间

适用市场：
- 牛市：效果最好
- 震荡市：频繁假突破
- 熊市：不适用

使用建议：
- 结合大盘：大盘向好时使用
- 设置止损：跌破年线止损
- 控制仓位：分批买入
- 耐心等待：等待回踩机会
"""

# ==================== 导入必需的库 ====================
import numpy as np  # 数值计算
import talib as tl  # 技术分析库
from datetime import datetime, timedelta  # 日期时间处理

__author__ = 'myh '
__date__ = '2023/3/10 '


# ==================== 回踩年线策略 ====================

"""
回踩年线策略检测函数
参数说明：
code_name (tuple): 股票信息(date, code, name)
data (DataFrame): 历史K线数据
必须包含：
- date：日期
- close：收盘价
- volume：成交量
- 至少250天数据（计算年线需要）
date (datetime.date, 可选): 计算日期
threshold (int): 观察周期，默认60天
- 在最近60天内寻找符合条件的形态
返回值：
bool:
- True：符合回踩年线策略
- False：不符合策略
策略条件详解：
条件1：时间段划分
- 前段：突破前的上升阶段
- 最高价：60日内的最高收盘价
- 后段：最高价之后的回调阶段
条件2：前段突破年线
- 开始：在年线下方（close < ma250）
- 结束：在年线上方（close > ma250）
- 说明：成功突破年线，进入上升趋势
条件3：后段不破年线
- 所有日期：close >= ma250
- 说明：回调有支撑，趋势未改变
- 回调时间：10-50日（适度回调）
条件4：缩量回调
- 最高价日放量：量大说明有资金推动
- 最低价日缩量：量小说明恐慌盘出局
- 量比 > 2：放量/缩量对比明显
- 价格回调 > 20%：回调充分
执行流程：
1. 数据预处理
2. 计算年线（MA250）
3. 取最后60天数据
4. 找出最高价和最低价
5. 划分前段和后段
6. 检查前段突破
7. 检查后段回踩
8. 检查缩量条件
9. 返回结果
实战案例：
某股票在60天内：
- 第1-20天：在年线下震荡（前段开始）
- 第21天：突破年线（前段结束）
- 第22-40天：在年线上继续上涨
- 第41天：创60日新高（后段开始）
- 第42-60天：回调但不破年线（后段）
- 第55天：创近期新低，缩量（回踩完成）
- 第60天：收盘（符合策略，买入信号）
"""
def check(code_name, data, date=None, threshold=60):
    
    # ==================== 步骤1: 数据预处理 ====================
    # 确定计算日期
    if date is None:
        end_date = code_name[0]
    else:
        end_date = date.strftime("%Y-%m-%d")

    # 筛选数据：只保留截止日期之前的数据
    if end_date is not None:
        mask = (data['date'] <= end_date)
        data = data.loc[mask].copy()
    
    # ==================== 步骤2: 数据充足性检查 ====================
    # 至少需要250天数据（计算年线）
    if len(data.index) < 250:
        return False

    # ==================== 步骤3: 计算年线 ====================
    # MA250：250日移动平均线（年线）
    data.loc[:, 'ma250'] = tl.MA(data['close'].values, timeperiod=250)
    # 处理NaN值（前249天没有年线）
    data['ma250'].values[np.isnan(data['ma250'].values)] = 0.0

    # ==================== 步骤4: 取最后60天数据 ====================
    # 只分析最近60天的形态
    data = data.tail(n=threshold)

    # ==================== 步骤5: 寻找区间最高价和最低价 ====================
    # 初始化：[价格, 成交量, 日期]
    lowest_row = [1000000, 0, '']  # 最低价记录（初始化为很大的数）
    highest_row = [0, 0, '']  # 最高价记录（初始化为0）
    recent_lowest_row = [1000000, 0, '']  # 近期最低价（后段用）

    # 遍历数据，找出最高价和最低价
    # zip()：同时遍历多个列
    for _close, _volume, _date in zip(data['close'].values, data['volume'].values, data['date'].values):
        # 更新最高价
        if _close > highest_row[0]:
            highest_row[0] = _close  # 价格
            highest_row[1] = _volume  # 成交量
            highest_row[2] = _date  # 日期
        # 更新最低价
        elif _close < lowest_row[0]:
            lowest_row[0] = _close
            lowest_row[1] = _volume
            lowest_row[2] = _date

    # 检查成交量是否有效
    if lowest_row[1] == 0 or highest_row[1] == 0:
        return False

    # ==================== 步骤6: 划分前段和后段 ====================
    # 前段：最高价日之前的数据（突破阶段）
    data_front = data.loc[(data['date'] < highest_row[2])]
    # 后段：最高价日及之后的数据（回调阶段）
    data_end = data.loc[(data['date'] >= highest_row[2])]

    # 前段必须有数据（不能从第一天就是最高）
    if data_front.empty:
        return False
    
    # ==================== 步骤7: 条件2 - 检查前段突破年线 ====================
    # 前段开始时：在年线下方
    # 前段结束时：在年线上方
    # 说明：成功突破年线
    if not (data_front.iloc[0]['close'] < data_front.iloc[0]['ma250'] and
            data_front.iloc[-1]['close'] > data_front.iloc[-1]['ma250']):
        # 不是从年线下突破到年线上，不符合
        return False

    # ==================== 步骤8: 条件3 - 检查后段不破年线 ====================
    if not data_end.empty:
        # 后段必须在年线以上运行（回踩但不跌破）
        # 同时寻找后段的最低价
        for _close, _volume, _date, _ma250 in zip(
            data_end['close'].values, 
            data_end['volume'].values, 
            data_end['date'].values, 
            data_end['ma250'].values
        ):
            # 检查是否跌破年线
            if _close < _ma250:
                # 跌破年线，趋势改变，不符合
                return False
            
            # 更新后段最低价
            if _close < recent_lowest_row[0]:
                recent_lowest_row[0] = _close
                recent_lowest_row[1] = _volume
                recent_lowest_row[2] = _date

    # ==================== 步骤9: 检查回调时间 ====================
    # 计算最低价日与最高价日的间隔
    # strptime()：字符串转日期
    # datetime.date()：提取日期部分
    date_diff = datetime.date(datetime.strptime(recent_lowest_row[2], '%Y-%m-%d')) - \
                datetime.date(datetime.strptime(highest_row[2], '%Y-%m-%d'))

    # 回调时间必须在10-50日之间
    # 太短：回调不充分
    # 太长：趋势可能已改变
    if not (timedelta(days=10) <= date_diff <= timedelta(days=50)):
        return False
    
    # ==================== 步骤10: 条件4 - 检查缩量回调 ====================
    # 量比：最高价日成交量 / 最低价日成交量
    vol_ratio = highest_row[1] / recent_lowest_row[1]
    
    # 回调幅度：最低价 / 最高价
    back_ratio = recent_lowest_row[0] / highest_row[0]

    # 检查缩量和回调幅度
    # vol_ratio > 2：上涨时放量，回调时缩量
    # back_ratio < 0.8：回调超过20%
    if not (vol_ratio > 2 and back_ratio < 0.8):
        return False

    # 所有条件满足，符合策略
    return True


