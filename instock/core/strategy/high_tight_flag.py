"""
高而窄的旗形策略检测函数
⚠️ 高风险高收益策略！谨慎使用！
参数说明：
code_name (tuple): 股票信息(date, code, name)
data (DataFrame): 历史K线数据
必须包含：
- date：日期
- close：收盘价
- high：最高价
- low：最低价
- p_change：涨跌幅
date (datetime.date, 可选): 计算日期
threshold (int): 观察周期，默认60天
istop (bool): 是否龙虎榜有机构
- True：有机构参与
- False：没有机构
- 必须为True才执行策略
返回值：
bool:
- True：符合高而窄的旗形策略
- False：不符合策略
策略条件详解：
前提条件：istop=True
- 龙虎榜上有机构席位
- 说明：主力资金参与
- 不是散户资金炒作
条件1：上市时间
- 至少60天
- 有足够历史数据
条件2：大幅上涨（"高"）
- 当日最高价 / 前期最低价 >= 1.9
- 涨幅至少90%
- 说明：快速大幅上涨
- 这是旗杆（高）
条件3：连续涨停（"窄"）
- 在10-24天前的区间内
- 必须有连续2天涨停
- 涨停：p_change >= 9.5%
- 说明：上涨集中，时间短
- 这是窄幅快速上涨
"高而窄"的含义：
高：
- 涨幅大（>=90%）
- 从低点快速拉升
窄：
- 时间短（10-24天）
- 上涨集中（连续涨停）
- 不是缓慢上涨
执行流程：
1. 检查机构参与（前提）
2. 数据预处理
3. 检查上市时间
4. 取前24~10天的数据
5. 计算涨幅比例
6. 检查是否连续涨停
7. 返回结果
为什么是前24~10天？
- 60天太长
- 10天太短
- 24-10天正好是旗杆形成期
- 避免太早或太晚的涨停
"""
def check_high_tight(code_name, data, date=None, threshold=60, istop=False):
    
    # ==================== 前提条件：检查机构参与 ====================
    # istop：龙虎榜机构标志
    # 必须为True（有机构参与）
    if not istop:
        # 没有机构参与，不符合策略
        # 高而窄的旗形需要主力资金支撑
        return False
    
    # ==================== 步骤1: 数据预处理 ====================
    # 确定计算日期
    if date is None:
        end_date = code_name[0]
    else:
        end_date = date.strftime("%Y-%m-%d")
    
    # 筛选数据
    if end_date is not None:
        mask = (data['date'] <= end_date)
        data = data.loc[mask]
    
    # ==================== 步骤2: 条件1 - 检查上市时间 ====================
    # 至少需要60天数据
    if len(data.index) < threshold:
        return False

    # ==================== 步骤3: 取最后60天数据 ====================
    data = data.tail(n=threshold)

    # ==================== 步骤4: 取前24~10天的数据（旗杆期）====================
    # 取最后24天
    data = data.tail(n=24)
    # 再取前14天（相当于倒数第24到第10天）
    # head(14) = tail(24)[0:14] = 第37-24天
    data = data.head(n=14)
    
    # 现在data包含的是：倒数第24天到倒数第10天的数据
    # 这个区间用于检查快速上涨（旗杆）

    # ==================== 步骤5: 条件2 - 检查大幅上涨 ====================
    # 找出这14天的最低价
    low = data['low'].values.min()
    
    # 计算涨幅比例
    # 用最后一天的最高价 / 区间最低价
    # 注意：这里用的是data.iloc[-1]['high']
    # data已经是14天的数据，所以iloc[-1]是第14天（倒数第10天）
    ratio_increase = data.iloc[-1]['high'] / low
    
    # 检查涨幅是否>=1.9（90%）
    if ratio_increase < 1.9:
        # 涨幅不足，不够"高"
        return False

    # ==================== 步骤6: 条件3 - 检查连续涨停 ====================
    # 遍历这14天，寻找连续涨停
    previous_p_change = 0.0  # 前一天的涨跌幅
    
    for _p_change in data['p_change'].values:
        # 检查当天是否涨停（涨幅>=9.5%）
        if _p_change >= 9.5:
            # 当天涨停
            # 检查前一天是否也涨停
            if previous_p_change >= 9.5:
                # 连续两天涨停！符合条件
                return True
            else:
                # 前一天不是涨停，记录当天涨幅
                previous_p_change = _p_change
        else:
            # 当天不是涨停，重置计数
            previous_p_change = 0.0

    # 没有找到连续涨停
    return False


