"""
放量跌停策略检测函数
⚠️ 高风险策略！仅供学习参考！
参数说明：
code_name (tuple): 股票信息(date, code, name)
data (DataFrame): 历史K线数据
必须包含：
- date：日期
- close：收盘价
- volume：成交量
- p_change：涨跌幅
date (datetime.date, 可选): 计算日期
threshold (int): 需要的数据天数，默认60天
返回值：
bool:
- True：符合放量跌停策略（可能是抄底机会）
- False：不符合策略
策略条件详解：
条件1：跌停
- p_change <= -9.5%
- 接近10%跌停限制
- 说明：市场极度悲观
条件2：成交额充足
- 成交额 >= 2亿
- 说明：流动性好，不是无量下跌
- 避免：小盘股操纵
条件3：放巨量
- 成交量 / 5日均量 >= 4
- 说明：恐慌盘集中释放
- 对比：放量上涨只需要2倍
- 跌停时要求更高（4倍）
为什么条件这么严？
- 跌停风险极高
- 必须确认是恐慌性杀跌
- 不是趋势性下跌
- 避免抄底抄在半山腰
执行流程：
1. 数据预处理
2. 检查跌幅（是否跌停）
3. 计算5日均量
4. 检查成交额
5. 检查放量倍数
6. 返回结果
反弹预期：
成功概率：约50-60%
反弹幅度：5-15%
反弹时间：1-5天
⚠️ 不保证盈利！
"""
def check(code_name, data, date=None, threshold=60):
    
    # ==================== 步骤1: 数据预处理 ====================
    # 确定计算日期
    if date is None:
        end_date = code_name[0]
    else:
        end_date = date.strftime("%Y-%m-%d")
    
    # 筛选数据
    if end_date is not None:
        mask = (data['date'] <= end_date)
        data = data.loc[mask].copy()
    
    # 数据充足性检查
    if len(data.index) < threshold:
        return False

    # ==================== 步骤2: 条件1 - 检查跌幅 ====================
    # 获取当天涨跌幅
    p_change = data.iloc[-1]['p_change']
    
    # 检查是否跌停（跌幅 >= 9.5%）
    if p_change > -9.5:
        # 跌幅不够，不是跌停
        # 注意：p_change是负数，-9.5表示跌了9.5%
        # p_change > -9.5 表示跌幅小于9.5%
        return False

    # ==================== 步骤3: 计算5日均量 ====================
    # 计算5日平均成交量
    data.loc[:, 'vol_ma5'] = tl.MA(data['volume'].values, timeperiod=5)
    # 处理NaN值
    data['vol_ma5'].values[np.isnan(data['vol_ma5'].values)] = 0.0

    # 取最后61天数据（60天+当天）
    data = data.tail(n=threshold + 1)
    
    # 再次检查数据充足性
    if len(data.index) < threshold + 1:
        return False

    # ==================== 步骤4: 获取当天数据 ====================
    # 最后一天的收盘价
    last_close = data.iloc[-1]['close']
    # 最后一天的成交量
    last_vol = data.iloc[-1]['volume']

    # ==================== 步骤5: 条件2 - 检查成交额 ====================
    # 计算成交额 = 价格 * 成交量
    amount = last_close * last_vol

    # 成交额必须不低于2亿
    if amount < 200000000:
        # 成交额太小，流动性不足
        return False

    # ==================== 步骤6: 获取5日均量 ====================
    # 去掉最后一天，取倒数第二天的5日均量
    data = data.head(n=threshold)
    
    # 获取5日均量（最后一天的值）
    mean_vol = data.iloc[-1]['vol_ma5']

    # ==================== 步骤7: 条件3 - 检查放量倍数 ====================
    # 计算量比：当日量 / 5日均量
    vol_ratio = last_vol / mean_vol
    
    # 检查是否放量4倍以上
    if vol_ratio >= 4:
        # 巨量跌停，可能是恐慌性杀跌
        # 恐慌盘释放后，可能反弹
        return True
    else:
        # 放量不够，可能还会继续下跌
        return False


