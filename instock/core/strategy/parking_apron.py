"""
停机坪策略（第六层 - 短线策略）
=================================
这是一个短线爆发形态策略，寻找涨停后横盘整理的股票。

策略名称：停机坪
策略类型：短线爆发、形态突破
策略周期：短期（3-5天）

策略原理：
- 股票先涨停（强势标志）
- 涨停后横盘整理（不回调）
- 形成"停机坪"形态
- 整理后可能继续上涨

什么是停机坪形态？
- 形态图像：像飞机停机的平台
- 结构：涨停+横盘
- 特征：高位缩量横盘
- 预期：蓄势待发，可能再次上涨

策略条件（3个主要条件）：
1. 近15日有涨停（涨幅>9.5%）：
   - 且必须是放量上涨（使用海龟策略判断）
   - 说明：有主力资金推动
   
2. 涨停后第1天：
   - 必须高开（open > 前收盘）
   - 收盘价上涨（close > 前收盘）
   - 涨幅不大（close/open: 0.97-1.03，约±3%）
   - 说明：不大涨不大跌，开始横盘
   
3. 涨停后第2-3天：
   - 必须高开
   - 收盘价上涨
   - 涨幅控制（close/open: 0.97-1.03）
   - 每天涨跌幅在±5%之间
   - 说明：继续横盘，筹码稳定

为什么这样设计？
- 涨停放量：有主力资金介入
- 高开不跌：主力控盘能力强
- 小幅震荡：洗盘吸筹，筹码换手
- 形成平台：蓄势待发

策略优势：
1. 形态明显：容易识别
2. 主力控盘：涨停+横盘说明有资金
3. 买点明确：横盘结束突破时买入
4. 盈亏比好：止损明确，盈利空间大

策略风险：
1. 可能是诱多：涨停吸引跟风盘，主力出货
2. 横盘失败：可能转为下跌
3. 时间窗口短：需要快速决策
4. 容易被套：追高风险

适用市场：
- 牛市：效果最好
- 热点题材：容易出现
- 不适合熊市

使用建议：
- 关注成交量：横盘时量要缩小
- 设置止损：跌破平台止损
- 快进快出：短线操作
- 控制仓位：风险较高
"""

