"""
低ATR成长策略检测函数
参数说明：
code_name (tuple): 股票信息(date, code, name)
data (DataFrame): 历史K线数据
必须包含：
- date：日期
- close：收盘价
- p_change：涨跌幅
- 至少250天数据
date (datetime.date, 可选): 计算日期
ma_short (int): 短期均线周期（未使用）
ma_long (int): 长期均线周期（用于数据充足性检查）
threshold (int): 观察周期，默认10天
- 在最近10天计算ATR和成长性
返回值：
bool:
- True：符合低ATR成长策略
- False：不符合策略
策略条件详解：
条件1：上市时间
- len(data) >= 250
- 至少有250天历史数据
- 避免新股（波动大）
条件2：低波动
- ATR < 10%
- ATR = 平均每天涨跌幅的绝对值
- 说明：价格变化平稳
条件3：有成长
- 最高价 / 最低价 > 1.1
- 10天内涨幅超过10%
- 说明：虽然波动小，但在上涨
ATR计算方法（简化版）：
本策略使用简化的ATR计算：
- 不是标准的True Range
- 而是平均每天涨跌幅的绝对值
- total_change / days_count
- 更简单，足够用
执行流程：
1. 数据预处理
2. 检查数据充足性（>=250天）
3. 取最后10天数据
4. 遍历计算：ATR、最高最低价
5. 检查ATR是否<10%
6. 检查涨幅是否>10%
7. 返回结果
为什么这样计算ATR？
标准ATR：
- 需要高低价、前收盘价
- 计算复杂
简化ATR：
- 只用涨跌幅
- 计算简单
- 效果类似
"""
def check_low_increase(code_name, data, date=None, ma_short=30, ma_long=250, threshold=10):
    
    # ==================== 步骤1: 数据预处理 ====================
    # 确定计算日期
    if date is None:
        end_date = code_name[0]
    else:
        end_date = date.strftime("%Y-%m-%d")
    
    # 筛选数据
    if end_date is not None:
        mask = (data['date'] <= end_date)
        data = data.loc[mask]
    
    # ==================== 步骤2: 条件1 - 检查上市时间 ====================
    # 必须至少有250天数据
    if len(data.index) < ma_long:
        # 上市时间不足250天，不符合
        return False

    # 注释掉的代码：
    # 原本可能想计算均线，但后来没用
    # 保留代码方便以后扩展
    # data.loc[:, 'ma_short'] = tl.MA(data['close'].values, timeperiod=ma_short)
    # data['ma_short'].values[np.isnan(data['ma_short'].values)] = 0.0
    # data.loc[:, 'ma_long'] = tl.MA(data['close'].values, timeperiod=ma_long)
    # data['ma_long'].values[np.isnan(data['ma_long'].values)] = 0.0

    # ==================== 步骤3: 取最后10天数据 ====================
    data = data.tail(n=threshold)
    
    # 初始化统计变量
    inc_days = 0  # 上涨天数
    dec_days = 0  # 下跌天数
    days_count = len(data.index)  # 总天数
    
    # 再次检查数据充足性
    if days_count < threshold:
        return False

    # ==================== 步骤4: 初始化价格统计 ====================
    # 区间最低点（初始化为很大的数）
    lowest_row = 1000000
    # 区间最高点（初始化为0）
    highest_row = 0

    # 累计涨跌幅（绝对值）
    total_change = 0.0
    
    # ==================== 步骤5: 遍历计算 ====================
    # 遍历最后10天的数据
    for _close, _p_change in zip(data['close'].values, data['p_change'].values):
        # 统计上涨/下跌天数和幅度
        if _p_change > 0:
            # 上涨日
            total_change += abs(_p_change)  # 累加涨幅（绝对值）
            inc_days = inc_days + 1  # 上涨天数+1
        elif _p_change < 0:
            # 下跌日
            total_change += abs(_p_change)  # 累加跌幅（绝对值）
            dec_days = dec_days + 1  # 下跌天数+1
        # 平盘日（p_change == 0）：不计入

        # 更新最高价和最低价
        if _close > highest_row:
            highest_row = _close  # 更新最高价
        elif _close < lowest_row:
            lowest_row = _close  # 更新最低价

    # ==================== 步骤6: 条件2.1 - 计算并检查ATR ====================
    # 计算平均波动率（简化的ATR）
    # ATR = 总涨跌幅 / 总天数
    # 例如：10天总共涨跌50%，ATR = 50/10 = 5%
    atr = total_change / days_count
    
    # 检查ATR是否小于10%
    if atr > 10:
        # 波动太大，不符合"低ATR"
        return False

    # ==================== 步骤7: 条件2.2 - 检查成长性 ====================
    # 计算涨幅比例
    # ratio = (最高价 - 最低价) / 最低价
    # 例如：最高12元，最低10元，ratio = (12-10)/10 = 0.2 = 20%
    ratio = (highest_row - lowest_row) / lowest_row

    # 检查涨幅是否超过10%（1.1倍）
    if ratio > 1.1:
        # 注意：这里ratio>1.1表示涨幅>110%
        # 应该是ratio>0.1表示涨幅>10%
        # 可能是代码bug，或者是特殊设计
        # 按原代码逻辑：要求10天涨幅超过110%
        # 这个要求非常高，可能需要调整
        return True

    return False


