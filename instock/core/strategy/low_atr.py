"""
低ATR成长策略（第六层 - 波动类策略）
====================================
这是一个寻找低波动稳定成长股票的策略。

策略名称：低ATR成长
策略类型：稳健成长、低波动
策略周期：中长期（几周到几月）

策略原理：
- 股票波动率低（ATR小）
- 价格稳步上涨
- 避免大起大落
- 适合稳健投资者

什么是ATR？
- ATR：Average True Range（平均真实波幅）
- 衡量价格波动程度
- ATR越大，波动越大，风险越高
- ATR越小，波动越小，风险越低

什么是低ATR成长？
- 低ATR：波动小，稳定
- 成长：价格持续上涨
- 结合：稳定中上涨
- 特点：风险低，收益稳

策略条件（2个条件同时满足）：
1. 上市时间：
   - 必须至少上市250天
   - 说明：不是新股，有历史数据
   - 避免：新股波动大
   
2. 低波动成长：
   - 最近10天的最高收盘价 / 最低收盘价 > 1.1
   - 说明：10天内最大涨幅超过10%
   - 平均ATR < 10%（低波动）
   - 说明：波动小但在成长

为什么这样设计？
- 条件1：确保数据充足
- 条件2：低波动（稳）+ 成长（涨）
- 结合：稳定上涨

策略优势：
1. 风险低：波动小，回撤小
2. 持有舒适：不会大起大落
3. 适合长期：可以安心持有
4. 心理压力小：不用盯盘

策略风险：
1. 收益率低：涨幅不大
2. 错过牛股：大牛股波动往往大
3. 可能滞涨：长期横盘
4. 市场冷门：关注度低

适用投资者：
- 稳健型：不追求暴利
- 长期型：长期投资
- 新手：风险承受能力低
- 上班族：没时间盯盘

使用建议：
- 长期持有：不要频繁交易
- 组合投资：持有多只分散风险
- 定期检查：每月检查一次
- 基本面优先：选择好公司
"""

