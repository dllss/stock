# ==================== 放量上涨策略 ====================

"""
放量上涨策略检测函数
策略说明：
寻找温和上涨且成交量放大的股票
策略条件：
1. 涨幅 < 2% 或 收盘 < 开盘（避免追高）
2. 成交额 >= 2亿（流动性充足）
3. 成交量 / 5日均量 >= 2（放量）
参数说明：
code_name (tuple): 股票信息
- code_name[0]：日期（str）
- code_name[1]：代码（str）
- code_name[2]：名称（str）
data (DataFrame): 历史K线数据
必须包含：
- date：日期
- open：开盘价
- close：收盘价
- volume：成交量
- p_change：涨跌幅
date (datetime.date, 可选): 计算日期
- None：使用code_name中的日期
- 指定日期：用于批量计算
threshold (int): 最少需要的数据天数
- 默认60：至少需要60天数据
- 用于计算5日均量等指标
返回值：
bool: 
- True：符合放量上涨策略
- False：不符合策略
执行流程：
1. 数据预处理
2. 检查数据充足性
3. 条件1：涨幅检查
4. 条件2：成交额检查
5. 条件3：放量检查
6. 返回结果
使用场景：
- strategy_data_daily_job调用
- 遍历所有股票，筛选符合条件的
- 并行执行，提高效率
策略优化建议：
- 调整涨幅阈值：改为1%或3%
- 调整成交额阈值：根据市场情况
- 调整放量倍数：改为1.5倍或3倍
- 增加其他条件：如趋势、形态等
"""
def check_volume(code_name, data, date=None, threshold=60):
    
    # ==================== 步骤1: 数据预处理 ====================
    # 确定计算日期
    if date is None:
        # 使用stock信息中的日期
        end_date = code_name[0]
    else:
        # 使用指定日期
        end_date = date.strftime("%Y-%m-%d")
    
    # 筛选数据：只保留截止日期之前的数据
    if end_date is not None:
        mask = (data['date'] <= end_date)  # 创建布尔掩码
        data = data.loc[mask].copy()  # 筛选并复制数据
    
    # ==================== 步骤2: 数据充足性检查 ====================
    # 检查数据是否充足
    if len(data.index) < threshold:
        # 数据不足，无法计算（比如新股上市不久）
        return False

    # ==================== 步骤3: 条件1 - 涨幅检查 ====================
    """
    条件1：温和上涨，不追高
    - 涨幅 < 2%：避免追涨
    - 或收盘 < 开盘：当天回落
    
    为什么这样设置？
    - 涨幅太大容易追高，风险大
    - 收盘低于开盘说明有回调，相对安全
    - 寻找温和上涨机会
    """
    # 获取当天涨跌幅
    p_change = data.iloc[-1]['p_change']  # iloc[-1]：最后一行
    
    # 检查涨幅条件
    if p_change < 2 or data.iloc[-1]['close'] < data.iloc[-1]['open']:
        # 不符合条件1，直接返回False
        # 这个条件是"宽松"条件，用于初步筛选
        # 如果连这个都不满足，就不用检查后面的了
        return False

    # ==================== 步骤4: 计算5日均量 ====================
    """
    计算成交量的5日移动平均
    用于判断是否放量
    """
    # 计算5日均量
    # tl.MA()：移动平均函数
    # data['volume'].values：成交量数组
    # timeperiod=5：5日周期
    data.loc[:, 'vol_ma5'] = tl.MA(data['volume'].values, timeperiod=5)
    
    # 处理NaN值（前4天没有5日均值）
    data['vol_ma5'].values[np.isnan(data['vol_ma5'].values)] = 0.0

    # 取最后threshold+1天数据（用于后续计算）
    data = data.tail(n=threshold + 1)
    
    # 再次检查数据充足性
    if len(data) < threshold + 1:
        return False

    # ==================== 步骤5: 条件2 - 成交额检查 ====================
    """
    条件2：成交额不低于2亿
    
    为什么要这个条件？
    - 确保股票有足够流动性
    - 避免小盘股容易被操纵
    - 方便买卖，不会因为没人买卖而被套
    
    成交额计算：
    - 成交额 = 收盘价 * 成交量
    - 注意：成交量单位是股，不是手
    """
    # 获取最后一天的收盘价和成交量
    last_close = data.iloc[-1]['close']  # 收盘价
    last_vol = data.iloc[-1]['volume']  # 成交量（股）

    # 计算成交额
    # 成交额 = 价格 * 数量
    amount = last_close * last_vol

    # 检查成交额是否达标
    # 2亿 = 200,000,000
    if amount < 200000000:
        # 成交额不足2亿，流动性太差
        return False

    # ==================== 步骤6: 条件3 - 放量检查 ====================
    """
    条件3：成交量 / 5日均量 >= 2
    
    什么是放量？
    - 成交量显著大于近期平均水平
    - 说明有资金流入
    - 可能是主力建仓或散户跟风
    
    为什么用2倍？
    - 1倍不算放量，只是正常
    - 2倍以上才算明显放量
    - 可以根据需要调整（如1.5倍或3倍）
    
    量比计算：
    - 量比 = 当日成交量 / N日平均成交量
    - 本策略：量比 = 当日成交量 / 5日平均成交量
    """
    # 去掉最后一天（因为我们要用倒数第二天的5日均量）
    # head(n)：取前n行，相当于去掉最后一行
    data = data.head(n=threshold)

    # 获取5日均量（最后一天的值）
    mean_vol = data.iloc[-1]['vol_ma5']

    # 计算量比
    # 量比 = 当日量 / 平均量
    vol_ratio = last_vol / mean_vol
    
    # 检查是否放量
    if vol_ratio >= 2:
        # 成交量是平均量的2倍以上，符合放量条件
        return True
    else:
        # 没有明显放量，不符合策略
        return False


