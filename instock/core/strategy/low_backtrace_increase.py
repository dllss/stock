"""
无大幅回撤策略检测函数
参数说明：
code_name (tuple): 股票信息(date, code, name)
data (DataFrame): 历史K线数据
必须包含：
- date：日期
- close：收盘价
- open：开盘价
- p_change：涨跌幅
- 至少60天数据
date (datetime.date, 可选): 计算日期
threshold (int): 观察周期，默认60天
返回值：
bool:
- True：符合无大幅回撤策略
- False：不符合策略（有大幅回撤）
策略条件详解：
条件1：60日涨幅 >= 60%
- (当日收盘价 - 60天前收盘价) / 60天前收盘价 >= 0.6
- 说明：中期有明显上涨
- 不是横盘或下跌
条件2：无大幅回撤（关键）
检查4种回撤情况：
2a) 单日跌幅 > 7%
- p_change < -7
- 说明：单日暴跌
- 风险：恐慌性杀跌
2b) 高开低走 > 7%（单日）
- (close - open) / open * 100 < -7
- 说明：开盘涨，收盘跌
- 风险：主力出货迹象
2c) 两日累计跌幅 > 10%
- 今天跌幅 + 昨天跌幅 < -10
- 说明：连续下跌
- 风险：趋势改变
2d) 两日高开低走累计 > 10%
- (今天收盘 - 昨天开盘) / 昨天开盘 < -10%
- 说明：连续出货
- 风险：主力撤退
为什么这些情况要排除？
- 单日大跌：恐慌盘，风险高
- 高开低走：出货迹象
- 连续下跌：趋势改变
- 连续出货：主力撤离
- 都是风险信号！
执行流程：
1. 数据预处理
2. 取最后60天数据
3. 计算60日涨幅
4. 检查涨幅是否>=60%
5. 遍历每一天，检查是否有大幅回撤
6. 只要有一次大幅回撤，就不符合
7. 返回结果
注释中的"允许一次洗盘"：
代码中有注释：# 允许有一次"洗盘"
但实际代码中没有实现
只要有一次大跌，就返回False
可能是后续可以优化的方向
"""
def check(code_name, data, date=None, threshold=60):
    
    # ==================== 步骤1: 数据预处理 ====================
    # 确定计算日期
    if date is None:
        end_date = code_name[0]
    else:
        end_date = date.strftime("%Y-%m-%d")
    
    # 筛选数据
    if end_date is not None:
        mask = (data['date'] <= end_date)
        data = data.loc[mask]
    
    # 数据充足性检查（至少60天）
    if len(data.index) < threshold:
        return False

    # ==================== 步骤2: 取最后60天数据 ====================
    data = data.tail(n=threshold)

    # ==================== 步骤3: 条件1 - 检查60日涨幅 ====================
    # 计算60日涨幅
    # ratio = (当日收盘 - 60天前收盘) / 60天前收盘
    ratio_increase = (data.iloc[-1]['close'] - data.iloc[0]['close']) / data.iloc[0]['close']
    
    # 检查涨幅是否>=60%（0.6）
    if ratio_increase < 0.6:
        # 涨幅不足60%，不符合
        return False

    # ==================== 步骤4: 条件2 - 检查是否有大幅回撤 ====================
    # 初始化前一天的数据
    previous_p_change = 100.0  # 前一天涨跌幅（初始值很大，不影响第一天）
    previous_open = -1000000.0  # 前一天开盘价（初始值很小，不影响第一天）
    
    # 遍历60天的数据
    for _p_change, _close, _open in zip(
        data['p_change'].values, 
        data['close'].values, 
        data['open'].values
    ):
        # 检查4种大幅回撤情况
        # 只要出现一种，就返回False
        
        # 情况1：单日跌幅 > 7%
        if _p_change < -7:
            # 单日暴跌，不符合
            return False
        
        # 情况2：高开低走 > 7%（当日）
        # (close - open) / open * 100：当日高开低走幅度
        if (_close - _open) / _open * 100 < -7:
            # 高开后大幅回落，不符合
            return False
        
        # 情况3：两日累计跌幅 > 10%
        # previous_p_change + _p_change：两日跌幅累计
        if previous_p_change + _p_change < -10:
            # 连续两天累计跌10%，不符合
            return False
        
        # 情况4：两日高开低走累计 > 10%
        # (今收 - 昨开) / 昨开：两日高开低走累计
        if (_close - previous_open) / previous_open * 100 < -10:
            # 连续出货，不符合
            return False
        
        # 没有触发任何回撤条件，继续检查下一天
        # 更新前一天的数据
        previous_p_change = _p_change  # 记录当天涨跌幅
        previous_open = _open  # 记录当天开盘价
    
    # ==================== 步骤5: 返回结果 ====================
    # 所有天都没有大幅回撤，符合策略
    return True


