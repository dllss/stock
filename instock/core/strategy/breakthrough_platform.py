"""
突破平台策略（第六层 - 突破类策略）
====================================
这是一个经典的突破策略，寻找横盘后向上突破的股票。

策略名称：突破平台
策略类型：横盘突破、趋势启动
策略周期：中短期（1-3周）

策略原理：
- 股价长期横盘整理（围绕60日均线震荡）
- 某天放量突破60日均线
- 横盘平台被突破，趋势启动
- 可能进入上升阶段

什么是平台突破？
- 平台：股价在一定区间内横盘
- 突破：价格向上突破平台上沿
- 放量：成交量显著放大
- 意义：多空平衡被打破，趋势启动

策略条件（3个主要条件）：
1. 突破形态：
   - 60日内某天：开盘价 < 60日均线 <= 收盘价
   - 说明：当天向上突破60日均线
   
2. 突破放量：
   - 突破当天必须放量上涨
   - 使用enter策略的放量判断（量比>=2）
   
3. 横盘整理：
   - 突破前的时间段
   - 收盘价与60日均线偏离在-5%~20%之间
   - 说明：围绕均线横盘，没有大幅偏离

为什么这样设计？
- 条件1：确认突破发生
- 条件2：确认有资金推动
- 条件3：确认之前在横盘（不是追高）

策略优势：
1. 形态清晰：容易识别
2. 风险可控：有平台支撑
3. 趋势启动：突破后空间大
4. 止损明确：跌破平台止损

策略风险：
1. 假突破：突破后又跌回
2. 追高风险：突破日可能涨幅较大
3. 平台定义：不同人定义不同
4. 时间成本：等待横盘需要时间

适用市场：
- 震荡市转牛市：最佳时机
- 个股独立行情：也适用
- 不适合熊市

使用建议：
- 观察平台：至少横盘10天以上
- 确认放量：成交量显著放大
- 等待回踩：突破后回踩平台再买入
- 设置止损：跌破平台5%止损
"""

# ==================== 导入必需的库 ====================
from datetime import datetime  # 日期处理
import numpy as np  # 数值计算
import talib as tl  # 技术分析库
from instock.core.strategy import enter  # 放量上涨策略（用于判断放量）

__author__ = 'myh '
__date__ = '2023/3/10 '


# ==================== 突破平台策略 ====================

"""
突破平台策略检测函数
参数说明：
code_name (tuple): 股票信息(date, code, name)
data (DataFrame): 历史K线数据
必须包含：
- date：日期
- close：收盘价
- open：开盘价
- volume：成交量
- 至少60天数据
date (datetime.date, 可选): 计算日期
threshold (int): 观察周期，默认60天
返回值：
bool:
- True：符合突破平台策略
- False：不符合策略
策略逻辑：
1. 在60天内寻找突破点
2. 突破点：open < ma60 <= close
3. 突破点必须放量
4. 突破前所有天：围绕ma60横盘（-5%~20%）
突破点定义：
- 开盘价在均线下方
- 收盘价在均线上方
- 说明当天向上突破
横盘定义：
- 收盘价与均线偏离小
- -5%到20%之间
- 说明围绕均线震荡
为什么是-5%~20%？
- -5%：允许小幅跌破（假跌破或测试）
- +20%：允许一定程度上涨（不是大幅偏离）
- 这个范围定义了"横盘"
执行流程：
1. 数据预处理
2. 计算60日均线
3. 取最后60天数据
4. 遍历寻找突破点
5. 检查突破点是否放量
6. 检查突破前是否横盘
7. 返回结果
"""
def check(code_name, data, date=None, threshold=60):
    
    # ==================== 步骤1: 数据预处理 ====================
    origin_data = data  # 保存原始数据（供放量判断使用）
    
    # 确定计算日期
    if date is None:
        end_date = code_name[0]
    else:
        end_date = date.strftime("%Y-%m-%d")
    
    # 筛选数据
    if end_date is not None:
        mask = (data['date'] <= end_date)
        data = data.loc[mask].copy()
    
    # 数据充足性检查（至少60天）
    if len(data.index) < threshold:
        return False

    # ==================== 步骤2: 计算60日均线 ====================
    # MA60：60日移动平均线（季线）
    data.loc[:, 'ma60'] = tl.MA(data['close'].values, timeperiod=60)
    # 处理NaN值
    data['ma60'].values[np.isnan(data['ma60'].values)] = 0.0

    # ==================== 步骤3: 取最后60天数据 ====================
    data = data.tail(n=threshold)

    # ==================== 步骤4: 寻找突破点 ====================
    breakthrough_row = None  # 突破日期
    
    # 遍历所有数据，寻找突破点
    for _close, _open, _date, _ma60 in zip(
        data['close'].values, 
        data['open'].values, 
        data['date'].values, 
        data['ma60'].values
    ):
        # 检查是否满足突破条件：
        # open < ma60 <= close
        # 说明：开盘在均线下，收盘在均线上，向上突破
        if _open < _ma60 <= _close:
            # 检查是否放量（调用enter策略的放量判断）
            # enter.check_volume检查：
            # 1. 涨幅<2%或收盘<开盘
            # 2. 成交额>=2亿
            # 3. 量比>=2
            if enter.check_volume(
                code_name, 
                origin_data, 
                date=datetime.date(datetime.strptime(_date, '%Y-%m-%d')), 
                threshold=threshold
            ):
                # 找到放量突破点
                breakthrough_row = _date
                break  # 找到就退出循环

    # ==================== 步骤5: 检查是否找到突破点 ====================
    if breakthrough_row is None:
        # 没有找到符合条件的突破点
        return False

    # ==================== 步骤6: 检查突破前是否横盘 ====================
    # 筛选突破日之前的数据
    # 且ma60 > 0（有效的均线值）
    data_front = data.loc[(data['date'] < breakthrough_row) & (data['ma60'] > 0)]
    
    # 遍历突破前的所有数据
    for _close, _ma60 in zip(data_front['close'].values, data_front['ma60'].values):
        # 计算收盘价与均线的偏离度
        # (ma60 - close) / ma60
        deviation = (_ma60 - _close) / _ma60
        
        # 检查偏离度是否在-5%~20%之间
        # -0.05 < deviation < 0.2
        # 等价于：close在ma60的95%-120%之间
        if not (-0.05 < deviation < 0.2):
            # 有一天偏离太大，不是横盘
            return False

    # 所有条件满足，符合突破平台策略
    return True


