"""
海龟交易法则 - 入场条件检测
策略说明：
当股价突破60日新高时买入
策略条件：
1. 当日收盘价 >= 最近60日最高收盘价
参数说明：
code_name (tuple): 股票信息(date, code, name)
data (DataFrame): 历史K线数据
date (datetime.date, 可选): 计算日期
threshold (int): 突破周期
- 默认60：60日突破（系统2）
- 可改为20：20日突破（系统1）
返回值：
bool:
- True：突破新高，买入信号
- False：未突破，不买入
执行流程：
1. 数据预处理
2. 检查数据充足性
3. 取最后60天数据
4. 计算60日最高价
5. 比较当日价格
6. 返回结果
策略逻辑：
假设最近60天最高价是100元
如果今天收盘价>=100元
则认为突破新高，买入
为什么用收盘价？
- 收盘价是当天最终确定的价格
- 避免假突破（盘中突破但收盘回落）
- 更可靠的信号
为什么是60日？
- 海龟交易的经典参数
- 约3个月的交易日
- 中长期趋势
- 可以调整为20、40、120等
"""
def check_enter(code_name, data, date=None, threshold=60):
    
    # ==================== 步骤1: 数据预处理 ====================
    # 确定计算日期
    if date is None:
        end_date = code_name[0]
    else:
        end_date = date.strftime("%Y-%m-%d")
    
    # 筛选数据：只保留截止日期之前的数据
    if end_date is not None:
        mask = (data['date'] <= end_date)
        data = data.loc[mask]
    
    # ==================== 步骤2: 数据充足性检查 ====================
    # 至少需要threshold天数据（默认60天）
    if len(data.index) < threshold:
        # 数据不足60天，无法判断（如新股）
        return False

    # ==================== 步骤3: 取最后60天数据 ====================
    # tail(n)：取最后n行
    # 只用最后60天数据，提高效率
    data = data.tail(n=threshold)

    # ==================== 步骤4: 计算60日最高价 ====================
    # 遍历所有收盘价，找到最大值
    max_price = 0  # 初始化最高价
    for _close in data['close'].values:
        if _close > max_price:
            max_price = _close  # 更新最高价

    # ==================== 步骤5: 获取当日收盘价 ====================
    # iloc[-1]：最后一行
    last_close = data.iloc[-1]['close']

    # ==================== 步骤6: 判断是否突破 ====================
    # 如果当日收盘价 >= 60日最高价
    if last_close >= max_price:
        # 突破新高，买入信号
        return True

    # 未突破，不买入
    return False


