"""
均线多头策略（第六层 - 趋势类策略）
====================================
这是一个经典的趋势跟踪策略，寻找均线呈多头排列的股票。

策略名称：均线多头（持续上涨）
策略类型：趋势跟踪、均线系统
策略周期：中长期（1-3个月）

策略原理：
- 30日均线持续向上
- 说明中期趋势向好
- 趋势一旦形成，往往会延续
- 顺势而为，胜率高

什么是均线多头排列？
- 短期均线 > 中期均线 > 长期均线
- 各周期均线都向上
- 表示多方力量占优
- 上升趋势确立

策略条件（2个条件同时满足）：
1. 均线递增：
   - 30天前的MA30 < 20天前的MA30 < 10天前的MA30 < 当天的MA30
   - 说明：30日均线持续向上，没有拐头
   
2. 上涨幅度：
   - 当天的MA30 / 30天前的MA30 > 1.2
   - 说明：30天内均线上涨超过20%
   - 趋势强劲

为什么用30日均线？
- 月线：约30个交易日 = 1个月
- 中期趋势：过滤短期波动
- 不太快：避免频繁交易
- 不太慢：及时跟上趋势

为什么要均线递增？
- 确保趋势没有拐头
- 每个时间点都在上升
- 趋势连贯性好
- 避免假突破

策略优势：
1. 趋势明确：均线向上清晰
2. 胜率较高：顺势而为
3. 持有舒适：不用频繁操作
4. 风险可控：均线拐头止损

策略风险：
1. 识别滞后：均线是滞后指标
2. 震荡市：频繁拐头，假信号
3. 止损可能大：趋势改变时已跌不少
4. 错过启动：趋势初期可能错过

适用市场：
- 牛市：最佳环境
- 结构性行情：个股独立趋势
- 不适合熊市和震荡市

使用建议：
- 结合大盘：大盘趋势向上时使用
- 等待回调：均线附近是买点
- 持股待涨：趋势不改不卖出
- 均线拐头：果断止损
"""

# ==================== 导入必需的库 ====================
import numpy as np  # 数值计算
import talib as tl  # 技术分析库

__author__ = 'myh '
__date__ = '2023/3/10 '


# ==================== 均线多头策略 ====================

"""
均线多头策略检测函数
参数说明：
code_name (tuple): 股票信息(date, code, name)
data (DataFrame): 历史K线数据
必须包含：
- date：日期
- close：收盘价
- 至少30天数据
date (datetime.date, 可选): 计算日期
threshold (int): 观察周期，默认30天
- 观察30天内均线的变化
返回值：
bool:
- True：符合均线多头策略
- False：不符合策略
策略条件详解：
条件1：均线递增
- 检查4个时间点的MA30
- 30天前、20天前、10天前、当天
- 必须依次递增
- 说明：均线持续向上，趋势稳定
条件2：上涨幅度
- 当天MA30 / 30天前MA30 > 1.2
- 说明：30天涨幅超过20%
- 趋势不仅向上，而且力度足
为什么分3段检查？
- step1 = 10天前（threshold/3）
- step2 = 20天前（threshold*2/3）
- 均匀分布
- 全面检查趋势
为什么要涨幅>20%？
- 确保趋势力度足够
- 不是微弱上涨
- 有持续性
执行流程：
1. 数据预处理
2. 计算30日均线
3. 取最后30天数据
4. 计算检查点位置
5. 检查均线递增
6. 检查上涨幅度
7. 返回结果
"""
def check(code_name, data, date=None, threshold=30):
    
    # ==================== 步骤1: 数据预处理 ====================
    # 确定计算日期
    if date is None:
        end_date = code_name[0]
    else:
        end_date = date.strftime("%Y-%m-%d")
    
    # 筛选数据
    if end_date is not None:
        mask = (data['date'] <= end_date)
        data = data.loc[mask].copy()
    
    # 数据充足性检查（至少30天）
    if len(data.index) < threshold:
        return False

    # ==================== 步骤2: 计算30日均线 ====================
    # MA30：30日移动平均线（月线）
    data.loc[:, 'ma30'] = tl.MA(data['close'].values, timeperiod=30)
    # 处理NaN值
    data['ma30'].values[np.isnan(data['ma30'].values)] = 0.0

    # ==================== 步骤3: 取最后30天数据 ====================
    data = data.tail(n=threshold)

    # ==================== 步骤4: 计算检查点位置 ====================
    # 将30天分为3段，均匀检查
    # round()：四舍五入取整
    step1 = round(threshold / 3)  # 约10天前
    step2 = round(threshold * 2 / 3)  # 约20天前
    
    # 现在有4个检查点：
    # - data.iloc[0]：30天前（第1天）
    # - data.iloc[step1]：20天前（第11天）
    # - data.iloc[step2]：10天前（第21天）
    # - data.iloc[-1]：当天（第30天）

    # ==================== 步骤5: 条件1和2 - 检查均线递增和涨幅 ====================
    # 检查两个条件：
    # 1. 均线递增：ma30[0] < ma30[step1] < ma30[step2] < ma30[-1]
    # 2. 上涨幅度：ma30[-1] > 1.2 * ma30[0]
    if (data.iloc[0]['ma30'] < data.iloc[step1]['ma30'] < 
        data.iloc[step2]['ma30'] < data.iloc[-1]['ma30'] and 
        data.iloc[-1]['ma30'] > 1.2 * data.iloc[0]['ma30']):
        # 两个条件都满足
        return True
    else:
        # 不满足
        return False


